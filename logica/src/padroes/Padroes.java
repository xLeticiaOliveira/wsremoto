package padroes;

public class Padroes {
	/*
	 * 
	 * Padrões:
	 * 
	 * - CamelCase: na composição com mais de uma palavra, da segunda deve-se iniciar com letra maiuscula.
	 * 		Variável: primeira letra minúscula (dataNascimento)
	 * 		Classe: primeira letra maiúscula (MyClass)
	 * 		Método: primeira letra minúscula, seguido de parênteses (myMethod())
	 * 		
	 * - Nomes significativos: coloque um nome para variável ou classe que faça sentido
	 * 
	 * 
	 * Regras:
	 * 
	 * 1. Não começarás um nome com número (1valor)
	 * 2. Não utilizarás palavras reservadas (void, public, int, etc.)
	 * 3. Não utilizarás caracteres especiais (d@t@)
	 * 
	 */
}
